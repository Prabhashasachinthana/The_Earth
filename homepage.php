The crust varies considerably in thickness, it is thinner under the oceans, thicker under the continents. The inner core and crust are solid; the outer core and mantle layers are plastic or semi-fluid. The various layers are separated by discontinuities which are evident in seismic data; the best known of these is the Mohorovicic discontinuity between the crust and upper mantle.<br><br>
